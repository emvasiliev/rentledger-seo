export const ALL_FORMS = [...CRA_FORMS, ...IRS_FORMS];

export function getFormBySlug(slug: string): TaxForm | undefined {
  return ALL_FORMS.find((f) => f.slug === slug);
}

export function getFormById(id: string): TaxForm | undefined {
  return ALL_FORMS.find((f) => f.id === id);
}
